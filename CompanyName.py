def Find_Company_Name(Dict):
  Company_Names = []
  for k,v in Dict.items():
    if k == 'filings':
      for x in v:
        for k,v in x.items():
          if k == 'entities':
            for x in v:
              for k,v in x.items():
                if k == 'companyName':
                  Company_Names.append(v)

  Amended_Company_Names = []
  for x in Company_Names:
    y = x.split(' ')
    y = y[:-1]
    x = str(' '.join(y))
    Amended_Company_Names.append(x)
  return(Amended_Company_Names)

